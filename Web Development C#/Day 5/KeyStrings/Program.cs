// "\n" - indicates a newline character
Console.WriteLine("List of items: \n1.Item1\n2.Item2");
// "\t" - indicates a tab character
Console.WriteLine("Number \t Name\n1 \t One");
// "\" - indicates a literal backslash character
Console.WriteLine("Displat the backslash: \\");
// "'" - indicates a single quote character (useful for character literals enclosed in single quotes)
Console.WriteLine("The computer said, \'Hello World\'");
// """ - indicates a double quote character (useful for string literals enclosed in double quotes)
Console.WriteLine("The computer said, \"Hello World\"");
// "\r" - indicates a carriage return character (used in some operating systems)
Console.WriteLine("Carriage return \r");

// "\b" - indicates a backspace character that deletes a single character
Console.WriteLine("indicates a  \bbackspace character");

// "\f" - indicates a form feed character
Console.WriteLine("indicates a \fform \ffeed character");

// "\v" - indicates a vertical tab character
Console.WriteLine("The computer \vsaid, \vHello World\v");

// "\uXXXX" - indicates a Unicode character with the hexadecimal value XXXX (e.g., "\u0041" represents the letter 'A')
Console.WriteLine("Unicode character: \u0071");

